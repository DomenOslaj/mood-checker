mood = input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Oh no :(")
elif mood == "excited":
    print("Great to see you excited!")
elif mood == "relaxed":
    print("Yoga bro? :)")
else:
    print("I don't recognize this mood.")
